    private void tableTurmasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableTurmasMouseClicked

        // retorna a linha da tabela onde o clique ocorreu
        int row = tableTurmas.rowAtPoint(evt.getPoint());
        // retorna o código do cliente selecionado
        Long id = (Long) tableTurmas.getModel().getValueAt(row, 0);

        try {
            turmaSelecionada = turmaDAO.buscarPorID(id);

            System.out.println(turmaSelecionada);

        } catch (Exception e) {
            e.printStackTrace();
        }
        txtTurmaID.setText(String.valueOf(turmaSelecionada.getId()));
    }//GEN-LAST:event_tableTurmasMouseClicked

