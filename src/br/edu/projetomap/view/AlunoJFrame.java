    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {

            Aluno aluno = alunosJFrame.getAlunoSelecionado();

            if (aluno != null) {

                String cpf = txtCPF.getText();
                String nome = txtNome.getText();
                String telefone = txtTelefone.getText();
//                Date dataNascimento = DateUtil.stringToDate(txtDataNascimeto.getText());
                Turma turma = turmaDAO.buscarPorID(Long.parseLong(txtTurmaID.getText()));
                // validar!

                aluno.setCpf(cpf);
                aluno.setNome(nome);
                aluno.setTelefone(telefone);
//                aluno.setDataNascimento(dataNascimento);
                aluno.setTurma(turma);

                alunoDAO.editar(aluno, turma);

                alunosJFrame.setAlunoSelecionado(null);

            } else {

                aluno = new Aluno();

                String cpf = txtCPF.getText();
                String nome = txtNome.getText();
                String telefone = txtTelefone.getText();
//                Date dataNascimento = DateUtil.stringToDate(txtDataNascimeto.getText());
                Turma turma = turmaDAO.buscarPorID(Long.parseLong(txtTurmaID.getText()));
                // validar!

                aluno.setCpf(cpf);
                aluno.setNome(nome);
                aluno.setTelefone(telefone);
//                aluno.setDataNascimento(dataNascimento);
                aluno.setTurma(turma);

                alunoDAO.inserir(aluno, turma);

            }

            alunosJFrame.preencherTabela(null);

            dispose();

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, MensagensUtil.getString(MensagensUtil.MSG_ERR0_SALVAR));
        }
    }//GEN-LAST:event_btnSalvarActionPerformed



