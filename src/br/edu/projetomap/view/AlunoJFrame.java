    public void preencherDados(){
        Aluno alunoSelecionado = alunosJFrame.getAlunoSelecionado();

        if (alunoSelecionado != null) {
            lblTitulo.setText("Editar Aluno");
            txtCPF.setText(alunoSelecionado.getCpf());
            txtNome.setText(alunoSelecionado.getNome());
            txtTelefone.setText(alunoSelecionado.getTelefone());
//            txtDataNascimeto.setText(DateUtil.dateToString(alunoSelecionado.getDataNascimento()));
            txtTurmaID.setText(String.valueOf(alunoSelecionado.getTurma().getId()));
        } else {
            lblTitulo.setText("Novo Aluno");
        }
    }
    
    public void preencherTabela(List<Turma> param) {

        try {

            List<Turma> lista = (param == null) ? turmaDAO.listar() : param;

            DefaultTableModel model = new DefaultTableModel();

            model.setColumnCount(4);
            model.setColumnIdentifiers(new String[]{
                "CPF", "NOME DO PROFESSOR", "TIPO", "AULA"});
            model.setRowCount(lista.size());

            for (int i = 0; i < lista.size(); i++) {
                model.setValueAt(lista.get(i).getId(), i, 0);
                model.setValueAt(lista.get(i).getNomeProfessor(), i, 1);
                model.setValueAt(lista.get(i).getTipo(), i, 2);
                model.setValueAt(lista.get(i).getAula(), i, 3);
            }

            tableTurmas.setModel(model);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

